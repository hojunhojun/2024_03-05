function Person(name) {
    this.name = name;
  }
  
  // 프로토타입 메서드
  Person.prototype.sayHello = function () {
    console.log(`Hi! My name is ${this.name}`);
  };
  
  const me = new Person('Lee');
  
  // hasOwnProperty는 Object.prototype의 메서드다.
  console.log(me.hasOwnProperty('name')); // true
  