class Numbers {
    numberArray = [];
  
    multiply(arr) {
      // 화살표 함수 내부에서 this를 참조하면 상위 스코프의 this를 그대로 참조한다.
      arr.forEach(item => this.numberArray.push(item * item));
    }
  }
  
  const numbers = new Numbers();
  numbers.multiply([1, 2, 3]);
  console.log(numbers.numberArray); // [1, 4, 9]
  